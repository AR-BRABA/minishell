#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	i = 0;
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (str1[i] && str1[i] == str2[i])
	{
		if (i < n - 1)
			i++;
		else
			return (0);
	}
	return (((unsigned char)s1[i]) - ((unsigned char)s2[i]));
}
